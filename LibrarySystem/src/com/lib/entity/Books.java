package com.lib.entity;

public class Books {
	private int bookId;
	private String title;
	private int price;
	private int edition;
	private int noFCopy;
	
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getEdition() {
		return edition;
	}
	public void setEdition(int edition) {
		this.edition = edition;
	}
	public int getNoFCopy() {
		return noFCopy;
	}
	public void setNoFCopy(int noFCopy) {
		this.noFCopy = noFCopy;
	}
	
}
